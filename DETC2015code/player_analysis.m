    % if data(consumption) is -1, it is a failed trial.

    if isempty(player_performance{data.userid})
        player_performance{data.userid} = score;
        if score>30
            wait = 1;
        end
    else
        player_performance{data.userid} = [player_performance{data.userid}, score];
    end
end

